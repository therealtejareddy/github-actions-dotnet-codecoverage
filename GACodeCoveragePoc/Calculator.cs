namespace GACodeCoveragePoc
{
    public class Calculator
    {
        public static int Add(int a, int b)
        {
            return a + b;
        }

        public static int Add(int a, int b, int c)
        {
            return a + b + c;
        }

        public static int Subtract(int a, int b)
        {
            return a - b;
        }
    }
}
